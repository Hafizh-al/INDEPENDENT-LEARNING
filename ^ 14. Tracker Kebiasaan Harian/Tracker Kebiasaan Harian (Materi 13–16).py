# Kenapa penting?
"""Bantu produktivitas dengan melacak kebiasaan baik."""

habits = {} # tempat simpan kebiasaan

while True:
    nama = input("Nama kebiasaan (ketik 'stop' untuk keluar): ")
    if nama.lower() == "stop":
        break
    jumlah = int(input("Berapa kali lakukan {nama} hari ini?: "))
    habits[nama] = jumlah

print("\nKebiasaan hari ini:")
for nama, jumlah in habits.items():
    print(f"{nama}: {jumlah} kali")
print(f"Total kebiasaan: {len(habits)}")
print(f"Total aktivitas: {sum(habits.values())} kali")
print("Teruskan kebiasaan baikmu!")
